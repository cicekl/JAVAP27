package edunova;

public class E01Varijable {

	public static void main(String[] args) {

		// varijable su prostor u memoriji

		// deklaracija varijable
		int i;

		// dodjeljivanje vrijednosti - operator dodjeljivanja
		i = 7;
		
		//korištenje vrijednosti varijable
		System.out.println(i);
		
		//deklaracija i dodjeljivanje odjednom
		int j = 8;
		
		// deklaracija više varijabli istog tipa
		int x,y,z;
		
		int r = j-i;
		
		System.out.println(r);
		

	}

}
