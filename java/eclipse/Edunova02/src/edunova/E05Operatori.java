package edunova;

public class E05Operatori {

	public static void main(String[] args) {

		int a = 2;
		int b = 7;

		float r = (float) a / b;

		System.out.println(r);

		// modulo %

		int i = 7 % 2;
		
		System.out.println(i);
		
		//nadoljepljivanje (concatination)
		
		System.out.println(i + " - rezultat modula.");
		
		
	}

}
