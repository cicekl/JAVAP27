    private void ucitaj(){
        DefaultListModel<Polaznik> m = 
                new DefaultListModel<>();
        m.addAll(obrada.read(
                txtUvjet.getText(),
                chbTraziOdPocetkaImena.isSelected()));
        lstPodaci.setModel(m);
        lstPodaci.repaint();
    }

